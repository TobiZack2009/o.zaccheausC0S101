fn main() {
    //Addition
    let sum:i32=5550+7310;
    println!("The sum of 5550 and 7310 is {}",sum );

    //Subtraction
    let difference:f32=95.5-4.3;
    println!("The difference between 95.5 and 4.3 is {}",difference );

    //Multiplication
    let product:i32=4*30;
    println!("The multiple between 4 and 30 is {}",product );

    //Quotient
    let quotient:f32=56.7/32.2;
    println!("The division of 56.7 and 32.2 = {}",quotient );

    //Remainder
    let remainder:u8=43%5;
    println!("The remainder of 43 and 5 is {}.",remainder );
}
